package important_demo_array_length;

public class ArrayLength {
    public static void main(String[] args) {
        int[] arr ={4,5,63,7,574,4,7,67,8};
        System.out.println(arr.length);     //数组长度是属性

        String s = "goodbye";
        System.out.println(s.length());     //字符串长度是方法

    }
}
